drop database if exists airport_terminal;

create database airport_terminal;

use airport_terminal;

create table terminal (
	id int auto_increment not null,
	title varchar(32) not null,
	removed boolean,
	primary key(id)
);

drop table if exists terminal_flight;

create table terminal_flight (
	terminal_id int not null,
	flight_id int not null,
	primary key(terminal_id, flight_id),
	foreign key(terminal_id) references terminal(id)
		on update cascade
		on delete cascade
);

drop table if exists terminal_employee;

create table terminal_employee (
	terminal_id int not null,
	employee_id int not null,
	primary key(terminal_id, employee_id),
	foreign key(terminal_id) references terminal(id)
		on update cascade
		on delete cascade
);

